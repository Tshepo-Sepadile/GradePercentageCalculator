    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        double myPercentage = (getMyGrade()/getTotal())*100;
        setPercentage(myPercentage);
        String thePercentage = Double.toString(getPercentage());
        jTextArea1.setText("*****PERCENTAGE*****");
        jTextArea1.setText("\nYour percentage is " + thePercentage);
        if(getPercentage() < 50 && getDone().equals("No"))
        {
            jTextArea1.setText(jTextArea1.getText() + "\nYou are failing.\nPull up your socks");
        }
        else if(getPercentage() >= 50 && getDone().equals("No"))
        {
            jTextArea1.setText(jTextArea1.getText() + "\nYou are passing.\nKeep it up.");
        }
        
        else if((getPercentage() < 50) && (getDone().equals("Yes")))
        {
            jTextArea1.setText(jTextArea1.getText() + "\nYou have failed.");
        }
        
        else if((getPercentage() >= 50) && (getDone().equals("Yes")))
        {
            jTextArea1.setText(jTextArea1.getText() + "\nYou have passed. Congratulations.");
        }
                        
    }//GEN-LAST:event_jButton3ActionPerformed

