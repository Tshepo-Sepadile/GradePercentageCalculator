    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        totalTot = 0;
        totalGrade = 0;
        int numOfTests = 0;
        while(numOfTests < testNum){
                        
        String totMark = JOptionPane.showInputDialog("What is the total mark?");
        total = Double.parseDouble(totMark);
        setTotal(total);
                        
                        
        String grade = JOptionPane.showInputDialog("What is your mark?");
        myGrade = Double.parseDouble(grade);
        setMyGrade(myGrade);
        
        while(myGrade > total)
        {
            grade = JOptionPane.showInputDialog("Grade cannot be greater than total! Re-enter: \nTotal:" + total);
            myGrade = Double.parseDouble(grade);
            setMyGrade(myGrade);
        }
                        
            setTestNum(testNum - 1);
                        
            totalTot = totalTot + getTotal();
            setTotal(totalTot);
                        
            totalGrade = totalGrade + getMyGrade();
            setMyGrade(totalGrade);
            
            String doneOrNot = JOptionPane.showInputDialog("Are you done for the year?");
            setDone(doneOrNot);
            }
        
        String theGrade = Double.toString(totalGrade);
        String theTotal = Double.toString(totalTot);
        jTextField1.setText(theGrade);
        jTextField2.setText(theTotal);
        
      
    }//GEN-LAST:event_jButton2ActionPerformed

